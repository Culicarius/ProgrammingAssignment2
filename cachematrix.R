
##The first function, makeVector creates a special list that contains functions to produce the results in cacheSolve
makeCacheMatrix <- function(x = matrix()) { 
     m <- NULL
     set <- function(y) {
          x <<- y
          m <<- NULL
     }
     get <- function() x
     setsolve <- function(solve) m <<- solve
     getsolve <- function() m
     list(set = set, get = get,
          setsolve = setsolve,
          getsolve = getsolve)
}
##If the inverse of the matrix has already been calculated, it will recover that from the cache, otherwise it will calculate it
cacheSolve <- function(x, ...) {
     m <- x$getsolve()
     if(!is.null(m)) {
          message("getting cached data")
          return(m)
     }
     data <- x$get()
     m <- solve(data, ...)
     x$setsolve(m)
     m
}
